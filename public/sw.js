// public/sw.js - VERSI√ìN COMPLETA Y SEGURA
const CACHE_NAME = 'mercury-app-v1';
const OFFLINE_CACHE = 'mercury-offline-v1';

// ‚úÖ Assets est√°ticos a cachear
const STATIC_ASSETS = [
  '/',
  '/index.html',
  '/manifest.json',
  '/favicon.ico',
  '/logo192.png',
  '/logo512.png'
];

// ‚úÖ URLs que deben ir directo a la red
const NETWORK_ONLY_URLS = [
  '/api/auth/',
  '/api/login',
  '/api/logout'
];

// ‚úÖ Evento INSTALL - Cache de assets est√°ticos
self.addEventListener('install', (event) => {
  console.log('üîÑ Service Worker installing...');
  
  event.waitUntil(
    Promise.resolve()
      .then(() => caches.open(CACHE_NAME))
      .then((cache) => {
        console.log('üì¶ Caching static assets');
        return cache.addAll(STATIC_ASSETS);
      })
      .then(() => {
        console.log('‚úÖ Service Worker installed successfully');
        return self.skipWaiting();
      })
      .catch((error) => {
        console.error('‚ùå Service Worker install failed:', error);
        // No re-throw para evitar crash
      })
  );
});

// ‚úÖ Evento ACTIVATE - Limpieza de caches antiguos
self.addEventListener('activate', (event) => {
  console.log('üöÄ Service Worker activating...');
  
  event.waitUntil(
    Promise.resolve()
      .then(() => caches.keys())
      .then((cacheNames) => {
        return Promise.all(
          cacheNames.map((cacheName) => {
            if (cacheName !== CACHE_NAME && cacheName !== OFFLINE_CACHE) {
              console.log('üóëÔ∏è Deleting old cache:', cacheName);
              return caches.delete(cacheName);
            }
          })
        );
      })
      .then(() => {
        console.log('‚úÖ Service Worker activated successfully');
        return self.clients.claim();
      })
      .catch((error) => {
        console.error('‚ùå Service Worker activation failed:', error);
        // No re-throw para evitar crash
      })
  );
});

// ‚úÖ Background Sync - Implementaci√≥n COMPLETA
self.addEventListener('sync', (event) => {
  console.log('üîÑ Background sync triggered:', event.tag);
  
  if (event.tag === 'background-sync') {
    event.waitUntil(syncOfflineData());
  }
});

// ‚úÖ FUNCI√ìN COMPLETA de sincronizaci√≥n offline
async function syncOfflineData() {
  try {
    console.log('üîÑ Starting background sync...');
    
    // ‚úÖ Timeout para evitar bloqueos infinitos
    const timeoutPromise = new Promise((_, reject) => {
      setTimeout(() => reject(new Error('Sync timeout after 30 seconds')), 30000);
    });
    
    // ‚úÖ Obtener token de autenticaci√≥n de forma segura
    const authTokenPromise = getAuthTokenSafely();
    
    // ‚úÖ Race entre sync y timeout
    const authToken = await Promise.race([authTokenPromise, timeoutPromise]);
    
    if (!authToken) {
      console.log('‚ö†Ô∏è No auth token available, skipping sync');
      return;
    }
    
    // ‚úÖ Realizar sincronizaci√≥n real
    const syncPromise = performDataSync(authToken);
    const result = await Promise.race([syncPromise, timeoutPromise]);
    
    console.log('‚úÖ Background sync completed successfully:', result);
    
    // ‚úÖ Notificar al cliente sobre el √©xito
    notifyClients({
      type: 'SYNC_SUCCESS',
      data: result
    });
    
    return result;
    
  } catch (error) {
    console.error('‚ùå Background sync failed:', error);
    
    // ‚úÖ Notificaci√≥n de error completa
    await self.registration.showNotification('Sync Failed', {
      body: 'Some data could not be synchronized. Will retry automatically.',
      icon: '/favicon.ico',
      badge: '/favicon.ico',
      tag: 'sync-failed',
      actions: [
        {
          action: 'retry',
          title: 'Retry Now'
        }
      ]
    });
    
    // ‚úÖ Notificar al cliente sobre el error
    notifyClients({
      type: 'SYNC_ERROR',
      error: error.message
    });
    
    // ‚úÖ NO re-throw para evitar crash
    return { error: error.message };
  }
}

// ‚úÖ Funci√≥n segura para obtener token de auth
async function getAuthTokenSafely() {
  try {
    // ‚úÖ Obtener clientes activos
    const clients = await self.clients.matchAll({ 
      includeUncontrolled: true,
      type: 'window'
    });
    
    if (clients.length === 0) {
      console.log('‚ö†Ô∏è No active clients found');
      return null;
    }
    
    // ‚úÖ Timeout para la petici√≥n de token
    return new Promise((resolve, reject) => {
      const timeout = setTimeout(() => {
        reject(new Error('Auth token request timeout'));
      }, 5000); // 5 segundos timeout
      
      const messageChannel = new MessageChannel();
      
      messageChannel.port1.onmessage = (event) => {
        clearTimeout(timeout);
        resolve(event.data.token);
      };
      
      // ‚úÖ Enviar petici√≥n al primer cliente
      clients[0].postMessage(
        { type: 'GET_AUTH_TOKEN' },
        [messageChannel.port2]
      );
    });
    
  } catch (error) {
    console.error('‚ùå Failed to get auth token:', error);
    return null;
  }
}

// ‚úÖ Implementaci√≥n real de sincronizaci√≥n
async function performDataSync(authToken) {
  try {
    // ‚úÖ Configuraci√≥n base de la petici√≥n
    const syncResponse = await fetch('/api/sync', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${authToken}`,
        'Content-Type': 'application/json'
      },
      body: JSON.stringify({
        lastSync: Date.now(),
        source: 'service-worker'
      })
    });
    
    if (!syncResponse.ok) {
      throw new Error(`Sync failed with status: ${syncResponse.status}`);
    }
    
    const syncData = await syncResponse.json();
    
    return {
      itemsCount: syncData.itemsCount || 0,
      lastSync: Date.now(),
      status: 'success'
    };
    
  } catch (error) {
    console.error('‚ùå Data sync request failed:', error);
    throw error;
  }
}

// ‚úÖ Funci√≥n para notificar a todos los clientes
async function notifyClients(message) {
  try {
    const clients = await self.clients.matchAll({
      includeUncontrolled: true,
      type: 'window'
    });
    
    clients.forEach(client => {
      try {
        client.postMessage(message);
      } catch (error) {
        console.error('‚ùå Failed to notify client:', error);
      }
    });
    
  } catch (error) {
    console.error('‚ùå Failed to get clients for notification:', error);
  }
}

// ‚úÖ Periodic Background Sync (si est√° soportado)
self.addEventListener('periodicsync', (event) => {
  console.log('‚è∞ Periodic background sync triggered:', event.tag);
  
  if (event.tag === 'periodic-sync') {
    event.waitUntil(syncOfflineData());
  }
});

// ‚úÖ Manejo de mensajes del cliente - IMPLEMENTACI√ìN COMPLETA
self.addEventListener('message', (event) => {
  try {
    console.log('üì® Message from client:', event.data);
    
    const { type, data } = event.data || {};
    
    switch (type) {
      case 'GET_AUTH_TOKEN':
        // ‚úÖ Este mensaje se maneja en la funci√≥n getAuthTokenSafely
        if (event.ports && event.ports[0]) {
          // El SW no tiene acceso directo a localStorage
          // Responder que no tiene el token
          event.ports[0].postMessage({ token: null });
        }
        break;
        
      case 'SKIP_WAITING':
        // ‚úÖ Forzar activaci√≥n del nuevo SW
        self.skipWaiting();
        break;
        
      case 'CHECK_UPDATE':
        // ‚úÖ Verificar actualizaciones
        checkForUpdates();
        break;
        
      default:
        console.log('‚ö†Ô∏è Unknown message type:', type);
    }
    
  } catch (error) {
    console.error('‚ùå Message handler error:', error);
    // ‚úÖ No re-throw para evitar crash
  }
});

// ‚úÖ Funci√≥n para verificar actualizaciones
async function checkForUpdates() {
  try {
    const registration = await self.registration.update();
    console.log('‚úÖ Update check completed');
    return registration;
  } catch (error) {
    console.error('‚ùå Update check failed:', error);
    return null;
  }
}

// ‚úÖ Evento FETCH - Estrategia de caching inteligente
self.addEventListener('fetch', (event) => {
  try {
    const url = new URL(event.request.url);
    
    // ‚úÖ Verificar si es una URL que debe ir directo a la red
    const isNetworkOnly = NETWORK_ONLY_URLS.some(networkUrl => 
      url.pathname.includes(networkUrl)
    );
    
    if (isNetworkOnly) {
      // ‚úÖ Network only para APIs de auth
      event.respondWith(fetch(event.request));
      return;
    }
    
    // ‚úÖ Cache First para assets est√°ticos
    if (STATIC_ASSETS.includes(url.pathname) || url.pathname.includes('.')) {
      event.respondWith(cacheFirst(event.request));
      return;
    }
    
    // ‚úÖ Network First para p√°ginas y APIs
    event.respondWith(networkFirst(event.request));
    
  } catch (error) {
    console.error('‚ùå Fetch handler error:', error);
    // ‚úÖ Fallback seguro
    event.respondWith(fetch(event.request));
  }
});

// ‚úÖ Estrategia Cache First
async function cacheFirst(request) {
  try {
    const cachedResponse = await caches.match(request);
    if (cachedResponse) {
      return cachedResponse;
    }
    
    const networkResponse = await fetch(request);
    const cache = await caches.open(CACHE_NAME);
    cache.put(request, networkResponse.clone());
    
    return networkResponse;
    
  } catch (error) {
    console.error('‚ùå Cache first strategy failed:', error);
    return new Response('Offline', { status: 503 });
  }
}

// ‚úÖ Estrategia Network First
async function networkFirst(request) {
  try {
    const networkResponse = await fetch(request);
    
    // ‚úÖ Cache successful responses
    if (networkResponse.ok) {
      const cache = await caches.open(CACHE_NAME);
      cache.put(request, networkResponse.clone());
    }
    
    return networkResponse;
    
  } catch (error) {
    console.error('‚ùå Network request failed, trying cache:', error);
    
    // ‚úÖ Fallback a cache
    const cachedResponse = await caches.match(request);
    if (cachedResponse) {
      return cachedResponse;
    }
    
    // ‚úÖ Fallback final
    if (request.mode === 'navigate') {
      return caches.match('/') || new Response('Offline', { 
        status: 503,
        headers: { 'Content-Type': 'text/html' }
      });
    }
    
    return new Response('Service Unavailable', { status: 503 });
  }
}

// ‚úÖ Manejo de notificaciones (si se usan)
self.addEventListener('notificationclick', (event) => {
  try {
    console.log('üîî Notification clicked:', event.notification.tag);
    
    event.notification.close();
    
    if (event.action === 'retry') {
      // ‚úÖ Retry sync on notification action
      event.waitUntil(syncOfflineData());
    }
    
    // ‚úÖ Abrir o enfocar la app
    event.waitUntil(
      self.clients.openWindow('/')
    );
    
  } catch (error) {
    console.error('‚ùå Notification click handler error:', error);
  }
});

